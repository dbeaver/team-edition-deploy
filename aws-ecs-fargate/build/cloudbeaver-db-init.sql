CREATE ROLE dc WITH LOGIN PASSWORD 'DCpassword';
CREATE ROLE qm WITH LOGIN PASSWORD 'QMpassword';
CREATE ROLE tm WITH LOGIN PASSWORD 'TMpassword';

CREATE SCHEMA IF NOT EXISTS dc;
CREATE SCHEMA IF NOT EXISTS qm;
CREATE SCHEMA IF NOT EXISTS tm;

GRANT ALL ON SCHEMA dc TO dc;
GRANT ALL ON SCHEMA qm TO qm;
GRANT ALL ON SCHEMA tm TO tm;
